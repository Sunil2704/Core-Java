package Array;

public class Blopping {
    public static void main(String[] args) {
        int a[]={44,55,77,3322,11,88};
        System.out.println(a.length);
        System.out.println("Printing Elements");
        for(int i=0;i<=a.length-1;i++)
        {
            System.out.println(a[i]);
        }
    }
}
